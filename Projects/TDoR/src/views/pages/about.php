This is a <i>prototype</i> of a site intended to serve as a companion resource for <a href="https://tdor.info/about-2/" target="_blank"><b>Transgender Day of Remembrance</b></a> (TDoR) events.</p>

<p><p>It came about as a result of my experience preparing for the 2017 vigil in Bournemouth, UK, - and which I wrote about in the blogpost <a href="https://medium.com/@annajayne/remembering-our-dead-never-gets-any-easier-befb5d7ee7cc" target="_blank"><b>Remembering Our Dead Never Gets Any Easier</b></a> (be warned: this blogpost is extremely hard to read).</p>

<p>Subsequently, I wrote up what we'd learned about each of the victims in a series of blogposts (one per month) linked together by  <a href="https://medium.com/@annajayne/tdor-2017-say-their-names-learn-their-stories-remember-them-b81d50fd8ef" target="_blank"><b>TDoR 2017: Say their names. Learn their stories. Remember them</b></a>.</p>

<p>Although I have since continued to publish reports in the same blogpost format, the long term aim has always been to look into the feasibility of using a dedicated site for this purpose.</p>

<p>This site aims to do exactly that. In the long term the aim is to migrate the TDoR 2017 data (and possibly that for earlier years) here, and offer exports of data and other resources that others can use to prepare for TDoR memorial events. If you would like to collaborate in developing things further, please let me know.</p>

In the meantime, I will obviously continue to publish TDoR memorial data as blogposts in the same way as before.</p>

