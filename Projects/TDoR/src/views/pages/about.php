<h3>Background</h3>

<p>The idea for the site came about as a direct result of the experiences of <a href="https://www.annasplace.me.uk/about" target="_blank" rel="noopener">Anna-Jayne Metcalfe</a> while collating information in preparation for the TDoR 2017 vigil in Bournemouth, United Kingdom. Afterwards she wrote the blogpost <a href="https://medium.com/@annajayne/remembering-our-dead-never-gets-any-easier-befb5d7ee7cc" target="_blank" rel="noopener"><b>Remembering Our Dead Never Gets Any Easier</b></a>. Please be warned that it is not an easy read.</p>

<p>Subsequently, what she learned about each of the victims was written up in a series of blogposts (one per month) starting at <a href="https://medium.com/@annajayne/tdor-2017-say-their-names-learn-their-stories-remember-them-b81d50fd8ef" target="_blank" rel="noopener"><b>TDoR 2017: Say their names. Learn their stories. Remember them</b></a>.</p>

<p>Although she also wrote <a href="https://medium.com/@annajayne/tdor-2018-say-their-names-read-their-stories-remember-them-2ccdccc9db1a" target="_blank" rel="noopener"><b>a similar series of blogposts for TDoR 2018</b></a>, the aim was always to develop a dedicated site for this purpose instead.</p>

<p><a href="https://medium.com/@annajayne/tdor-learning-more-about-those-we-have-lost-8043146f402c" target="_blank" rel="noopener"><b>This site aims to be just that</b></a>. It includes resources that can be used during TDoR memorial events, including data for trans murder and suicide victims dating back to 1970. If you need a suitable data set for TDoR research, you may well find here.</p>



